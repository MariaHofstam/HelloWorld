public class HelloWorld {

    public static void main(String[] args) {
        System.out.println("Hello World");
        System.out.println("Maria says hello GitHub");
        
        //Editing from GitHub
        System.out.println("GitHub says hello Maria!");

        //From Maria
        System.out.println("How are you?");
        
        //From GitHub
        System.out.println("I am just fine, Maria");

        System.out.println("I feel really awful today. I'm really sad");
        
        //From GitHub
        System.out.println("Ohh, I'm sorry to hear that. Hope you feel better soon");

        //From Maria
        System.out.println("Thanks GitHuh :-)");

    }
}
